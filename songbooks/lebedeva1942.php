Шура Лебедева. Альбом. Осташков, 1942-1945.<br>
<br>
Shura Lebedeva. Albom [Handwritten album]. Ostashkov, 1942-1945 (In Russian).<br>
<br>
<span class='i'>Альбом ученицы Осташковской средней школы №2 Шуры Лебедевой.
Блокнот, записи чернилами разных цветов. Клишированные альбомные стихи, 3 частушки
(одна из них на вложенном листе, другим почерком) и 6 песен.<br>
<br>
Сначала идут альбомные стихи, некоторые из них датированы 1942 годом.
Адресат стихов - некая Маня, в нескольких стихах ее имя исправлено на другие.
Потом пропуск - один разворот пустой.
 Четыре сдедующие записи - от 1 апр. 1944 г.: альбомный стих и три песни.
Следом песня без даты, песня с датой 8 дек. 1944 г., пара частушек и снова пропуск
 - два разворота пусты. Затем альбомный стих (возможно, означающий окончание войны),
 три пустых разворота и песня.
 Судя по прорезям в листах, в том числе в пустых, в альбом были вставлены картинки или фото.
 Некоторые записи помечены крестиком - это может говорить о том,
 что помеченные записи были переписаны в чистовой альбом, а блокнот - черновик.
 Тогда пропажа рисунков понятна: их тоже перенесли.<br>
<br>
Шура Лебедева училась в 7-м классе при заведении альбома - т.е. в 1942 г., а может,
даже в конце 1941 г. С осени 1941 г. до января 1943 г. Осташков находился у линии фронта,
некоторые предприятия были временно эвакуированы. Захвачен город не был.
Неизвестно, покидала ли город Шура.
В 1942 г. она заполняла альбом клишированными стихами; песни записала после того, как
фронт откатился на запад. К этому моменту Шура должна была окончить
8-летнюю школу, учиться в 9-10 классах, в профессиональном учебном заведении либо работать. <br>
<br>
Альбом получен от Алены Ивановой из Осташкова в мае 2020 г.<br>

