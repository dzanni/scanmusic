<br>
<span class='i'>Тетрадь Ф.С. Кириллова с песнями и стихотворениями, 1903 г.
	Из содержания следует, что Кириллов был из крестьян Владимирской губернии,
	жил в Санкт-Петербурге на ул. Большой Болотной (ныне Моисеенко), 24. 26 октября
	1903 г. (ст. ст.) подавал прошение об устройстве на работу служащим или рабочим
	на казенную пробочную фабрику, которая открылась в том году на  ул.
Новгородской, 13. Здание пробочной фабрики (обычно ее называют пробочным заводом)
сохранилось, дореволюционный дом по адресу Моисеенко, 24 - тоже, но, возможно,
был перестроен уже после 1903 г. <br>
	 <br>
Место хранения подлинника: РГАЛИ, Ф. 1346, оп. 4, ед. хр. 16, л. 14-37.<br></span>
