<span class='bold'>[3] Письмо с фронта<br></span>
<br>
Повезло нам сегодня с ночлегом:<br>
Печка топится, лампа горит,<br>
И подсолнух, окутанный снегом,<br>
Далеко на пригорке стоит.<br>
<br>
Чай дымится горячий и сладкий.<br>
Отогрелись мы в тёплой избе.<br>
На листочке из синей тетради<br>
Я пишу, дорогая, тебе.<br>
<br>
Я пишу, а кругом, дорогая,<br>
Спят бойцы на шинелях своих,<br>
Печка топится, лампа мигает,<br>
Свет спокойный бросая на них.<br>
<br>
Знаю я, ты прочтёшь эти строки,<br>
Улыбнёшься и скажешь опять:<br>
«Самый родненький ты мой на свете,<br>
Только писем не любишь писать».<br>
<br>
А потом возле детской кроватки,<br>
Возле нашего сына вздохнёшь<br>
И листочек из синей тетради<br>
К пухлым губкам своим поднесёшь.<br>
<br>
Скажешь: «Папочка это нам пишет,<br>
Хоть и редко, но письма он шлёт.<br>
Посмотри, погляди, как он дышит».<br>
И улыбка невольно блеснёт.<br>
<br>
Помню, милый, с тобой мы гуляли,<br>
Как цветы полевые росли,<br>
Но над нашей несчастной судьбою<br>
Вдруг фашисты топор занесли.<br>
<br>
Ничего, мы отрубим им руки<br>
...........................<br>
И за каждые горькие муки<br>
Враг поплатиться кровью своей.<br>
<br>
