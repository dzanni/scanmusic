<span class='bold'>[2] Есть на севере городок<br></span>
<br>
Есть на севере хороший городок.<br>
Он в лесах суровых северных залёг.<br>
Эх! Русская метелица там кружит, поёт.<br>
Там моя подруженька-душенька живёт.<br>
<br>
Письмоносец ей в окошко постучи.<br>
Письмецо моё заветное вручи.<br>
«Принимайте весточку с дальне стороны<br>
С поля вихря грозного – мировой войны».<br>
<br>
Слышишь, милая, далёкая моя,<br>
Охраняю я далёкие края.<br>
Эх! В ноченьки морозные, в ясные деньки<br>
В бой уходят грозные красные полки.<br>
<br>
Мы захватчиков-фашистов разобъём.<br>
С красными знамёнами по Родине пройдём.<br>
Эх! А война окончится, и настанет срок,<br>
Ворочусь я в северный милый городок.<br>
<br>
Я по небу – небу синему промчусь,<br>
Прямо к милому окошку опущусь,<br>
Постучу в окошечко, и душа замрёт.<br>
«Выходи, красавица, друг любезный ждёт».<br>
<br>
<span class='i'>27.04.1946 г.<br></span>
