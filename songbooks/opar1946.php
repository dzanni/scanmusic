<span class='bold'>[5] Молодёжь<br></span>
<br>
Две старушки за чайком<br>
Толковали вечерком:<br>
«Молодёжь так молодёжь!<br>
И поступки и слова.<br>
Лишь руками разведёшь,<br>
Разве это молодёжь?<br>
<br>
Вот племянницы мои<br>
Из порядочной семьи,<br>
И душа о них болит.<br>
Потеряли всякий стыд,<br>
Поглядишь, бросает в дрожь,<br>
Разве это молодёжь?<br>
<br>
У соседей на глазах<br>
Ходят в майках и трусах.<br>
«Физкультура,» - говорят,<br>
А по-нашему - разврат.<br>
Отвернёшься и вздохнёшь,<br>
Разве это молодёжь?<br>
<br>
Со знакомыми на «ты»,<br>
Смех и крик до хрипоты,<br>
И родителям ни в чём<br>
Не уступят нипочём.<br>
Вечно спор и галдёшь,<br>
Разве это молодёжь?<br>
<br>
Рассуждают так и сяк<br>
Про любовь, семью и брак,<br>
И для них в 15 лет<br>
Никаких загадок нет.<br>
Всё понятно им, ну что ж,<br>
Разве это молодёжь?<br>
<br>
Мы, бывало, ночью, днём<br>
Полны мыслями о нём.<br>
И мечтаешь, и поёшь,<br>
И тихонько слёзы льёшь.<br>
Нынче чувства ни на грош,<br>
Разве это молодёжь?<br>
<br>
Нету нежности к родным,<br>
Дядю выслали в Нарым,<br>
А они везде кричат:<br>
«Так и надо, дядя гад!»<br>
Отвернёшься и вздохнёшь,<br>
Разве это молодёжь?<br>
<br>
Я старалась к ним ходить<br>
И пыталась их стыдить.<br>
Мне ответили: «Вы хам,<br>
Не ходите больше к нам!»<br>
Что ж, и больше не пойдёшь,<br>
Разве это молодёжь?<br>
<br>
