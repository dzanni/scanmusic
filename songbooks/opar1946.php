<span class='bold'>[4] Песня<br></span>
<br>
Ты ходишь пьяная и вдрызг голодная<br>
По тёмным улицам Махач-Кала<br>
Тебе мерещится пол-литра верная,<br>
Кусок селёдки и колбаса.<br>
<br>
Прижавшись к автору авто бесшумного,<br>
Ты смотришь пристально в ряды огней,<br>
И в память прошлого полузабытого<br>
Мерцают призраки прошедших дней.<br>
<br>
И только лампочки с рассветом гасятся,<br>
Ты вся измучена к нему идёшь.<br>
Глаза янтарные, чуть-чуть печальные,<br>
Губы покусаны, сама пьяна.<br>
<br>
А муж послушный и равнодушный<br>
Свою жену в гостиной ждёт.<br>
Любви не зная, она покорная,<br>
Клянясь и плача, к нему идёт.<br>
<br>
А на диванчике подушки мятые,<br>
Ты полуголая в одних трусах.<br>
И вспомнишь ты тогда диваны мягкие,<br>
Вино шампанское и хор цыган.<br>
<br>
Пускай казачка тебя полюбит!<br>
Пускай владеет она тобой!<br>
Она любить так не сумеет,<br>
Как я любила тебя, мой дорогой.<br>
