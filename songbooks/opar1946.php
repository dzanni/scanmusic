<span class='bold'>[9] Не тревожь<br></span>
<br>
Не тревожь, ты меня, не тревожь,<br>
Обо мне ничего не загадывай,<br>
И когда мимо дома идёшь,<br>
На окошко моё не заглядывай.<br>
<br>
Зря записок ты мне не пиши,<br>
Фотографий своих не раздаривай.<br>
Голубые глаза хороши,<br>
Но а мне полюбилися карие.<br>
<br>
Полюбились любовью такой,<br>
Что вовек никогда не кончается.<br>
Вот он с фронта вернётся домой<br>
И под вечер со мной повстречается.<br>
<br>
Я прижму его к сердцу, прижму<br>
Молодыми руками горячими<br>
И всю правду ему скажу,<br>
Что обоим судьбой предназначены.<br>
<br>
А тебя об одном прошу,<br>
Ты напрасно меня не испытывай,<br>
Я на свадьбу тебя приглашу,<br>
А на большее ты не рассчитывай.<br>
