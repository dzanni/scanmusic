<span class='bold'>[6] Будьте здоровы!<br></span>
<br>
Будьте здоровы, живите богато,<br>
На сколько позволит Вам ваша зарплата,<br>
А если зарплата Вам жить не позволит,<br>
Ну, что ж, не живите: никто не неволит.<br>
<br>
Желаем, чтоб счастлив был каждый отныне,<br>
Всегда бы имели бы блат в магазине.<br>
В милицию тоже не суйтесь без блата,<br>
Там вам не пропишут ни тещу, ни брата.<br>
<br>
Что б люди на Вас не имели бы злости,<br>
Поменьше старайтесь ходить Вы к ним в гости,<br>
А если Вы в гости поехать хотите,<br>
Закуску и водку с собою берите.<br>
<br>
Еще пожелать Вашим дочкам осталось,<br>
Чтоб каждая шла за военного замуж,<br>
Об этом не может и быть разговора,<br>
Чтоб чином он был бы не ниже майора.<br>
<br>
Еще пожелать вам немного осталось,<br>
Чтоб в год по ребёнку у Вас нарождалось,<br>
Тогда «за бездетность» - забудьте ту фразу,<br>
Налоги платить перестанете сразу.<br>
<br>
Лет десять пройдет, если муж не разиня,<br>
Получите орден Вы «Мать-героиня».<br>
Окружат гурьбой Вас дочки, сынки,<br>
А как прокормить, Вы подумайте сами.<br>
