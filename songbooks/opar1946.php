<span class='bold'>[7] В лесу прифронтовом<br></span>
<br>
С берёз неслышен, невесом<br>
Слетает жёлтый лист.<br>
Старинный вальс «Осенний сон»<br>
Играет гармонист.<br>
<br>
Вздыхают, жалуясь басы,<br>
И, словно в забытьи,<br>
Сидят и слушают бойцы,<br>
Товарищи мои.<br>
<br>
Под этот вальс весенним днём<br>
Ходили мы на круг,<br>
Под этот вальс в краю родном<br>
Любили мы подруг.<br>
<br>
Под этот вальс любили мы<br>
Очей прекрасный свет,<br>
Под этот вальс грустили мы,<br>
Когда подруги нет.<br>
<br>
И вот он снова прозвучал<br>
В лесу прифронтовом.<br>
И каждый думал и мечтал<br>
О чём-то дорогом,<br>
<br>
И каждый думал о своей,<br>
Припомнив ту весну,<br>
И каждый знал дороги к ней<br>
Идут через войну.<br>
<br>
Так что ж, друзья, коль наш черёд,<br>
Да будет сталь крепка!<br>
Ведь наше сердце не умрёт,<br>
Не задрожит рука.<br>
<br>
Пусть свет и радость первых встреч<br>
Встретит в трудный час,<br>
А коль придётся в землю лечь,<br>
Так это только раз!<br>
<br>
То пусть и смерть в огне, в дыму<br>
Бойца не устрашит,<br>
И что положено кому,<br>
Пусть каждый совершит.<br>
<br>
Настал черёд, пришла пора,<br>
Идём, друзья, идём.<br>
За то, что жили мы вчера,<br>
За то, что завтра ждёт.<br>
