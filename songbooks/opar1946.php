<span class='bold'>[8] Клёш<br></span>
<br>
Его натура подобна шторму,<br>
Его фигура стройна, как мол.<br>
Ох, как же я, моряк, люблю морскую форму,<br>
Так поцелуй меня, моряк, и я твоя.<br>
<br>
Так поцелуй меня ты в щечку,<br>
А я подпрыгну под самый потолок.<br>
Пускай судьба пошлёт нам дочку,<br>
А может быть родиться и сынок.<br>
<br>
Родится сын, я назову Павлушой,<br>
Он будет стройный, морячок, как ты.<br>
А морячок, уткнувшийся в подушки,<br>
Давно уж спал под девичьи мечты.<br>
<br>
Итак, всю ночку Наточка не спала<br>
И сколько дела было в эту ночь.<br>
То сыну Павлику пелёночки меняла,<br>
То убаюкивала маленькую дочь.<br>
<br>
А морячок, сурово сдвинул брови.<br>
Смотреть – он был суровый негодяй.<br>
А он сказал: «Не порти моей крови,<br>
И ты о детях больше не мечтай».<br>
<br>
Итак, вы все – вы, глупые девчонки,<br>
Вы с ранних лет бросаетесь на клёш,<br>
А вместо клеша останутся пелёнки,<br>
А клеш с огнём уж больше не найдёшь.<br>
