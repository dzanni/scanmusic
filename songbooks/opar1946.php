<span class='bold'>[1] На горе белым бела<br></span>
<br>
На горе белым бела<br>
Рано вишня расцвела.<br>
Полюбила я мальчишку,<br>
Но завлечь не завлекла.<br>
<br>
Я по улицам хожу<br>
Об одном о нём тужу,<br>
И не разу он не спросит,<br>
Что я на сердце ношу.<br>
<br>
Только спросит: «Как живёте?<br>
Скоро ль в гости позовёте?»<br>
Только видно он не хочет<br>
Говорить по существу.<br>
<br>
Я одна иду домой,<br>
Вся печаль моя со мной.<br>
Неужели моё счастье<br>
Пронесётся стороной.<br>
<br>
