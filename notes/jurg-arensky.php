<br>
Жил-был менестрель в провансальской земле,<br>
В почете он жил при самом короле.<br>
Молчите, проклятые струны!<br>
<br>
Король был не ровня другим королям:<br>
Свой род возводил он к бессмертным богам...<br>
Молчите, проклятые струны!<br>
<br>
И дочь он, красавицу Берту, имел!<br>
Смотрел лишь на Берту певец, когда пел...<br>
Молчите, проклятые струны!<br>
<br>
Когда же он пел, то дрожала она,<br>
То вспыхнет огнем, то как мрамор бледна...<br>
Молчите, проклятые струны...<br>
<br>
И сам Император посватался к ней...<br>
Глядит менестрель всё угрюмей и злей...<br>
Молчите, проклятые струны!<br>
<br>
Дан знак менестрелю: когда будет бал,<br>
Чтоб в темной аллее у грота он ждал.<br>
Молчите, проклятые струны!<br>
<br>
Что было, чью руку лобзал он в слезах,<br>
И чей поцелуй у него на устах...<br>
Молчите, проклятые струны!<br>
<br>
Что Кесаря значит внезапный отъезд,<br>
Чей в склепе фамильном стоит новый крест,<br>
Из казней какую король изобрел,<br>
О чем с палачом долго речи он вел?<br>
Молчите, молчите, проклятые струны!<br>
<br>
Погиб менестрель, бедный вешний цветок,<br>
Король даже лютню разбил сам и сжег.<br>
Молчите, проклятые струны!<br>
<br>
И лютню он сжег, но не греза, не сон,<br>
Везде его лютни преследует звон.<br>
Молчите, проклятые струны!<br>
<br>
И слышит: незримые струны звучат<br>
И страшные ясно слова говорят...<br>
Молчите, проклятые струны!<br>
<br>
Не ест он, не пьет он и ночи не спит,<br>
Молчит, лишь порой, как безумный, кричит:<br>
"Молчите, молчите, проклятые струны!"<br>
<br>
Am Hof der Provence sich ein Sänger befand,<br>
Der hoch bei dem König in Ansehen stand.<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Dem Könige gleich war kein Herrscher der Welt:<br>
Es stammte von Göttern der mächtige Held.<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Und Bertha, sein Kind, war die Schönste im Land;<br>
Der Sänger beim Singen kein Aug' von ihr wandt...<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Sie lauschte dem Sange und bebte vor Weh,<br>
Bald rosig erglühend, bald weisser als Schnee...<br>
O, schweiget, ihr unsel'gen Saiten...<br>
<br>
Ein Kaiser umwarb sie von Liebe erfüllt,<br>
Da blickte der Sänger gar finster und wild...<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Dem Sänger ward Weisung zur Ballzeit allein<br>
Im dunkelsten Gange zur Grotte zu sein.<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Wer sagt's, wem er weinend geküsst hat die Hand,<br>
Wess Kuss ihm auf glühenden Lippen gebrannt...<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Wesshalb schied der Kaiser von Hofe bereits;<br>
Wess Grab ziert der Könisgruft neuestes Kreuz,<br>
Was hatte der König für Martern erdacht,<br>
Welch' Urtheil der Henker vollzogen bei Nacht?<br>
O, schweiget, o, schweiget doch, ihr unsel'gen Saiten!<br>
<br>
Der Sänger, die Blüthe des Lenzes, verschwand;<br>
Ergrimmt hat der König die Laute verbrannt.<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Und ob er's mit eigener Hand hat vollbracht,<br>
Verfolgt ihn ihr Jammern bei Tage und Nacht.<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Stets hört er der unsichtbarn Saiten Getön,<br>
Die klagend erzählen was einst hier gescheh'n...<br>
O, schweiget, ihr unsel'gen Saiten!<br>
<br>
Er isst nicht, er trinkt nicht, er schläft nicht mehr ein,<br>
Er schweigt, um bisweilen wie sinnlos zu schrein:<br>
"O, schweiget, o, schweiget, ihr unsel'gen Saiten!"<br>
