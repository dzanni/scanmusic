<br>
В раздумьи гордом и немом,<br>
Вперив в пространство взор глубокий,<br>
Орел на камне полевом<br>
Сидел, как странник одинокий.<br>
<br>
Крикливых птиц враждебный рой<br>
Над головой его кружился,<br>
И вихрь, взметая прах земной,<br>
Кругом испуганно носился.<br>
<br>
Но недвижим питомец скал,<br>
Не внемля суетному шуму,<br>
Орел на камне отдыхал<br>
И думал царственную думу.<br>
<br>
Когда ж, очнувшись, оглянул<br>
Весь этот жалкий мир бессилья,<br>
Затосковав, он разверул<br>
Седые, медленные крылья.<br>
<br>
И царь полуденных высот,<br>
Случайный гость земли печальной,<br>
Направил вольный свой полет<br>
За грани туч, к лазури дальной.<br>
<br>
Высоко от тщеты земной<br>
Вознесся, праху не подвластный, <br>
И, к дольной жизни безучастный,<br>
Исчезнул в бездне голубой.<br>
<br>
Ein Adler sass auf Felsgestein,<br>
Den Blick gerichtet in die Weite,<br>
Dem Pilger gleich, der ganz allein<br>
Tiefernstem Sinnen wird zur Beute.<br>
<br>
Der andern Vögel lauter Chor<br>
Umschwärmte ihn mit Schrein und Singen;<br>
Im Wirbelwind zu ihm empor,<br>
Versucht der Erdenstaub zu dringen.<br>
<br>
Doch schien er nicht die Vogelschaar,<br>
Noch auch den Wirbelwind zu hören.<br>
Es liess der königliche Aar <br>
In seinem Sinnen sich nicht stören.<br>
<br>
Genug des Sinnens! Einen Blick<br>
Des Erdentreibens nicht'gen Dingen,<br>
Dann kehrt zur Höhe er zurück,<br>
Ausbreitend die gewalt'gen Schwingen.<br>
<br>
Heimwärts zu seiner Väter Thron,<br>
Benachbart Sonne, Mond und Sternen,<br>
Nahm seinen Flug der Königssohn<br>
Zum Wolkensaum, zu blauen Fernen.<br>
<br>
Und Erdenstaub und Erdentand<br>
Hat bald er hinter sich gelassen,<br>
Und unberührt von Lieb und Hassen,<br>
Im blauen Aether er entschwand.<br>