<br>
В голубой, далекой спаленке<br>
Твой ребенок опочил.<br>
Тихо вылез карлик маленький<br>
И часы остановил.<br>
<br>
Все как было, только странная<br>
Воцарилась тишина,<br>
И в окне твоем туманная<br>
Только улица видна.<br>
<br>
И прошла ты, сонно белая,<br>
Вдоль по комнатам одна,<br>
Опустила, вся несмелая,<br>
Штору синего окна.<br>
<br>
И потом, едва заметная,<br>
Синий полог подняла,<br>
И, как время беспросветное,<br>
Шевелясь, поникла мгла.<br>
<br>
Стало тихо в дальней спаленке.<br>
Синий сумрак и покой,<br>
Потому что карлик маленький<br>
Держит маятник рукой.<br>