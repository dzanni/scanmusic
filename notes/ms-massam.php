Музсектор Госиздата образован в 1922 г. из Музиздата (Государственного музыкального издательства Наркомата просвещения).
Серия «Музыка массам» выходила под эгидой Всероссийского рабочего музыкального общества.
От имени Музсектора вышло около 200 номеров. В 1930 Музсектор преобразован в Музгиз,
в 1931 серия продолжена, а лакуны восполнены (например, №149 Музсектор не выпустил,
он был выпущен Музгизом в 1931). К лету 1931 Музгиз выпустил более 80 новых номеров «Музыки массам»,
затем серия переименована в <a href='../notes/mg-cheap'>«Дешевую
  библиотеку»</a>. Выпущенные Музсектором номера
«Музыки массам» были переизданы как «Дешевая библиотека» с пометкой «2-е изд.»<br>
