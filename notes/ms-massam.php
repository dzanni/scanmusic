Рыжичков, волвяночек,<br>
Белыих беляночек<br>
Наберу скорёшенько<br>
Я, млада-младёшенька,<br>
Что для свекра-батюшки,<br>
Для свекрови ль матушки:<br>
Перестали б скряжничать,<br>
Сели быпображничать.<br>
<br>
А тебе, немилому,<br>
Старому да хилому,<br>
Суну я в окошечко<br>
Целое лукошечко<br>
Мухомора старого,<br>
Старого, поджарого,<br>
Старый ест, не справится:<br>
Мухомором давится.<br>
<br>
А тебе, треклятому,<br>
Белу, кудреватому,<br>
Высмотрю я травушку,<br>
Травушку-муравушку,<br>
На постелю бранную,<br>
Свахой ночкой стланную,<br>
С пологом-дубровушкой<br>
Да со мной ли, вдовушкой.<br>