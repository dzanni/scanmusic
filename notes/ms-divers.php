Музсектор Госиздата образован в 1922 г. из Музиздата (Государственного музыкального издательства Наркомата просвещения).
В 1930 г. преобразован в Музгиз. Ноты печатались с новых досок и с дореволюционных - преимущественно
с досок издательства Юргенсона. В этом случае над старым номером доски ставился новый.<br>
