<span class='bold'>●</span> Дым без огня. Сл. и муз. А. Вертинского. Б303А<br>
<br>
Вот зима... На деревьях цветут снеговые улыбки...<br>
Я не верю, что в эту страну забредет Рождество.<br>
По утрам мой комичный maëstro так печально играет на скрипке,<br>
И в снегах голубых за окном мне поет Божество.<br>
<br>
Мне когда-то хотелось иметь золотого ребенка,<br>
А теперь я мечтаю уйти в монастырь, постареть.<br>
И молиться у старых притворов печально и тонко<br>
Или, может, совсем не молиться, а эти же песенки петь.<br>
<br>
Все бывает не так, как мечтаешь под лунные звуки,<br>
Всем понятно, что я не никуда не уйду.<br>
Что сейчас у меня есть обиды, долги, есть собака, любовница, муки,<br>
И что все это – так, пустяки, просто дым без огня.<br>