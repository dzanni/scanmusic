<span class='bold'>7.</span> Тебя любить, обнять и плакать над тобой. Исп. с громадным успехом
М.И. Вавичем. Н. Ширяев - А. Фет. Б198А.<br>
<br>
Сияла ночь. Луной был полон сад. Сидели<br>
Мы с тобой в гостиной без огней.<br>
Рояль был весь раскрыт, и струны в нем дрожали,<br>
Как и сердца у нас за песнию твоей.<br>
  <div class='refren'>Ты пела до зари, в слезах изнемогая,<br>
  Что ты одна — любовь, что нет любви иной,<br>
  И так хотелось жить, чтоб, звука не роняя,<br>
  Тебя любить, обнять и плакать над тобой!..</div>
<br>
Прошли года томительно и скучно,<br>
И вот в тиши ночной твой голос слышу вновь -<br>
И веет, как тогда, во вздохах этих звучных,<br>
Что ты одна — вся жизнь, что ты одна — любовь.<br>
  <div class='refren'>Что нет обид судьбы и сердца жгучей муки,<br>
  А жизни нет конца, и цели нет иной,<br>
  Как только веровать в рыдающие звуки,<br>
  Тебя любить, обнять и плакать над тобой!..</div>
