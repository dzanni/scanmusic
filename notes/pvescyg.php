<span class='bold'>13.</span> Вернись! Романс. Исполняется с громадным успехом
В.А. Сабининым. Фраксон - Неизвестный. (Интимные песенки). 1904.<br>
<br>
1. Комнатку нашу я вновь увидал,<br>
Где мы так часто встречались,<br>
Где я, бывало, так трепетно ждал<br>
Как во сне там счастья дни мчались.<br>
  <div class='refren'>И стал шептать мне вдруг каждый предмет:<br>
  Но отчего ты не с нею?<br>
  Ты здесь один без подруги своей.<br>
  Ах, отчего ж ее нет?</div>
<br>
<span class='i'>Припев:</span><br>
Чувства прежние пламенно в сердце зажглись<br>
И с тоской я шептал: Вернись!<br>
Вернись скорей! В разлуке с тобою так жутко;<br>
Только тобою одной я живу, бесконечно любя, молюся<br>
Вернись скорей! Вновь счастье меня озарит,<br>
Вновь буду с тобой ворковать до зари,<br>
Скорей, скорей приди!<br>
<br>
2. Вот позабытый тобою букет,<br>
Скромный букет из фиалок,<br>
Весь он отцвел, в нем и запаха нет;<br>
Вид его так грустен и жалок.<br>
  <div class='refren'>Плакал долго и горько над ним,<br>
  Старые раны открылись;<br>
  Счастья былого дни скрылись,<br>
  Горем одним я живу.</div>
<br>
<span class='i'>Припев.</span><br>
