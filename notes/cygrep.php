<span class='bold'>157.</span> У камина: Романс. Посв. и исп. Ниной Викторовной Дулькевич.
М.Е. Поливанова - Г. Китаев. И1540Н.<br>
<br>
1. Ты сидишь молчаливо и смотришь с тоской,<br>
Как печально камин догорает,<br>
Как в нем яркое пламя то вспыхнет порой,<br>
То бессильно опять угасает.<br>
<br>
2. Ты грустишь все о чем? Не о прошлых ли днях,<br>
Полных неги, любви и привета?<br>
Так чего же ты ищешь в сгоревших углях?<br>
О тебе не найти в них ответа.<br>
<br>
3. Подожди еще миг, и не будет огней,<br>
Что тебя так ласкали и грели,<br>
И останется груда лишь черных углей,<br>
Что сейчас догореть не успели.<br>
<br>
4. О! поверь, ведь любовь - это тот же камин,<br>
Где сгорают все лучшие грезы.<br>
А погаснет любовь, в сердце холод один,<br>
Впереди же страданья и слезы. <br>
