<span class='bold'>●</span> Как хороши, как свежи были розы. Л. Лисовский -
Стихотворение в прозе И. Тургенева. Ц.р. 1898. 24015 (Четыре фантазии для фортепиано
с сопровождением мелодекламации по желанию. Посв. Л.Е. Ефимович)<br>
<br>
Где-то, когда-то, давно, давно тому назад я прочел одно стихотворение. <br>
Оно скоро позабылось мною... <br>
Но первый стих остался у меня в памяти:<br>
<br>
"Как хороши, как свежи были розы".<br>
<br>
Теперь зима. <br>
Мороз запушил стекла окон; в темной комнате горит одна свеча.<br>
Я сижу, забившись в угол; а в голове все звенит да звенит: <br>
<br>
"Как хороши, как свежи были розы".<br>
<br>
И вижу я себя перед низким окном загородного русского дома. <br>
Летний вечер тихо тает и переходит в ночь. <br>
В теплом воздухе пахнет резедой и липой; а на окне, опершись на выпрямленную руку и
 склонив голову к плечу, сидит девушка — и безмолвно и пристально смотрит на небо,
 как бы выжидая появления первых звезд. <br>
Как простодушно вдохновенны задумчивые глаза. <br>
Как трогательно невинны раскрытые, вопрошающие губы, как ровно дышит еще не
вполне расцветшая, еще ничем не взволнованная грудь, как чист и нежен облик юного лица! <br>
Я не дерзаю заговорить с нею, но как она мне дорога, как бьется мое сердце!..<br>
<br>
"Как хороши, как свежи были розы..." <br>
<br>
А в комнате все темней да темней... <br>
Нагоревшая свеча трещит... беглые тени колеблются на низком потолке, мороз скрипит и
злится за стеной, и чудится скучный, старческий шепот...<br>
<br>
"Как хороши, как свежи были розы..."<br>
<br>
И встают передо мною другие образы.<br>
 Слышится веселый шум семейной деревенской жизни. <br>
 Две русые головки, прислонясь друг к дружке, бойко смотрят на меня своими
 светлыми глазками;  алые щеки трепещут сдержанным смехом; руки ласково сплелись;
 вперебивку звучат молодые, добрые голоса.<br>
 А немного подальше, в глубине уютной комнаты, другие, тоже молодые, руки бегают, путаясь
  пальцами, по клавишам старенького пианино, и ланнеровский вальс не может заглушить
  воркотни патриархального самовара.<br>
  <br>
"Как хороши, как свежи были розы…"<br>
<br>
Свеча меркнет и гаснет... <br>
Кто это кашляет там так хрипло и глухо... <br>
Свернувшись в калачик, жмется и вздрагивает у ног моих старый пес, мой единственный товарищ.<br>
 Мне холодно. <br>
 Я зябну. <br>
И все они умерли... умерли...<br>
<br>
"Как хороши, как свежи были розы…"<br>