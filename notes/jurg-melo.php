Изумрудной волной плещет море!<br>
О, бежим! Там узнаешь ты счастье со мной,<br>
Позабудешь житейское горе.<br>
<br>
Бремя прошлого сбросим, зароем на дно,<br>
Пусть уснет под водой быстротечной!<br>
