Как на озере на Ильмене,<br>
На крут береге изба стоит;<br>
Велика та изба - во все дерево,<br>
А лежит в ней на лавке сам Царь Морской.<br>
Залетел Соловей из Новгорода,<br>
Как запел по-соловьиному,<br>
Услыхал Царь Морской, распотешился,<br>
Золотых ему рыбок пожаловал.<br>
Как пришел Соловей в славный Новгород,<br>
Ударял Соловей о велик заклад;<br>
Тут повышли купцы новгородские,<br>
Лавки с красным товаром закладывали,<br>
Невод шелквый в воду закинули,<br>
А и золото красное вынули.<br>
Бедняками купцы те поделались,<br>
Соловей стал богатый торговый гость.<br>