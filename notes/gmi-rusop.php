в 1922 г. преобразовано в Музсектор Госиздата. Номера из опер русских композиторов печатались с дореволюционных досок с двойной
нумерацией - к старому номеру добавлялся новый. Фактически некоторые ноты были изданы уже
после преобразования в Музсектор Госиздата, но на обложке сохранялось указание на Музиздат.<br>
