Музсектор Госиздата образован в 1922 г. из Музиздата (Государственного музыкального издательства Наркомата просвещения).
В 1930 г. преобразован в Музгиз.<br>
<br>
Романсы С. Рахманинова - репринт с досок издательства <a href='../notes/gut-rakhm'>А. Гутхейля</a>, в старой
орфографии,
обычно с двойной нумерацией досок: рядом с номером доски Гутхейля ставился номер доски Музсектора. В серии Гутхейля
было 65 романсов. Музсектор сохранил их порядковый номер, но не печатал
романсы с заглавиями религиозной тематики №№ 12, 34, 34, 42, 57 (соответственно, Op.8 №6 "Молитва",
Op.21 №10 "Пред иконой", Op.21 №11 "Я не пророк", Op.26 №6 "Христос воскрес",
Op.34 №6 "Воскрешение Лазаря") и добавил к серии еще шесть произведений из Op. 38.
под номерами 66-71.
В конце 1920-х
некоторые романсы стали печатать с исправленных досок в новой орфографии.<br>
