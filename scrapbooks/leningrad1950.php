<span class='bold'>[2] Вдоль деревни<br></span>
<br>
Более двух десятилетий длилось творческое содружество замечательных советских художников
- Героя Социалистического Труда поэта Михаила Васильевича Исаковского и композитора,
народного артиста СССР Владимира Григорьевича Захарова. А началось оно с песни, о которой
пойдет речь, и началось, кстати, совершенно неожиданно.<br>
<br>
Это было в середине 30-х годов. Однажды Захаров, просматривая отрывной календарь,
обратил внимание на стихотворение Исаковского "Вдоль деревни", которое было написано
поэтом еще в 1925 году. Стихи понравились молодому композитору. Именно такие он давно
искал.<br>
<br>
Захаров в те годы вместе с П.М. Казьминым был художественным руководителем хора имени
Пятницкого. Репертуар коллектива состоял из народных русских песен, главным образом
дореволюционного происхождения. А Захарову хотелось создать для хора песню на
современную тему, причем такую, которая была бы в традициях народного хора, могла
бы органически войти в его концертную программу.<br>
<br>
Захаров почувствовал, что стихи Исаковского могут стать основой для такой песни.
Он принялся за работу. Часами композитор не отходил от инструмента, вновь и вновь
проигрывая музыкальные фразы, много раз повторяя отдельные места.<br>
<br>
"Эту песню я писал долго, - рассказывал он. - Я написал четыре варианта ее.
Мне хотелось использовать мастерство исполнения хора имени Пятницкого. Это было трудно.
Дело в том, что что в хоре в то время была очень сильная женская группа, а вот мужская
- чрезвычайно малоподвижна, поэтому пришлось написать для них как можно проще.
Так же просто пришлось написать и партию средних женских голосов, так как среди
исполнителей было много пожилых женщин, учивших с большим трудом".<br>
<br>
И вот как-то утром усталый, но довольный композитор призвал своих ближайших товарищей,
живших вместе с ним в небольшом бревенчатом домике, сел за старое, разбитое пианино
и сыграл только что написанную мелодию.  И всем стало ясно - песня удалась!<br>
<br>
Что касается автора стихов, то он понятия не имел обо всем этом. Но как-то
Исаковский увидел в кино звуковой журнал, в котором был показан хор имени Пятницкого,
исполнявший "Вдоль деревни".<br>
<br>
"Для меня, - писал в своих воспоминаниях М. Исаковский, - это был необычайно радостный
сюрприз: мое старое стихотворение вдруг стало песней, зазвучало потоком звонких
деревенских голосов".<br>
<br>
Радость поэта можно понять. Он никогда не предполагал, что на основе его стихов может
родиться песня. Правда, однажды Исаковский предложил музыкальному издательству свои
сочинения, но ему ответили: "Стихи ваши не годятся: для того чтобы писать песни, нужен
талант, а его-то у вас и нет". "На этом я и успокоился", - вспоминал поэт.<br>
<br>
Песня "Вдоль деревни" была, по существу, первенцем не только для Исаковского, но и для
Захарова, хотя до этого он уже написал одну песню ("По дорожке неровной") для хора
имени Пятницкого. Но настоящий успех и всеобщее признание принесла ему песня,
в которой просто и задушевно говорилось о том новом, что входило в жизнь и быт
колхозного села:<br>
<br>
Вдоль деревни, от избы и до избы,<br>
Зашагали торопливые столбы;<br>
Загудели, заиграли провода, -<br>
Мы такого не видали никогда.<br>
<br>
За годы своего сотрудничества М.В. Исаковский и В.Г. Захаров создали около
трех десятков песен, многие из которых вошли в золотой фонд советского песенного
творчества.<br>
<br>
А. Луковников<br>
