В начале июля 1944 года, начав наступление на минском направлении, наши войска
стремительно продвигались на запад. То и дело сменялись полевые аэродромы - едва
пригодные для посадок площадки. Эскадрилья штурмовиков, которой командовал капитан
Афанасьев, разместилась в деревушке, где уже до них остановилось несколько частей.
Из соседних лесов в поисках крова сюда же стекались местные жители - поближе к своим.
В ту памятную ночь пилоты-штурмовики устроились для ночевки кое-как, в наспех вырытых
щелях, едва прикрывающих людей от дождя, но не от бомб.<br>
<br>
Когда среди ночи налетели "юнкерсы" и заухали первые разрывы бомб, капитан Афанасьев
кинулся к стоянке своей эскадрильи; надо было спасать самолеты. Но вдруг среди
охваченных пламенем изб комэск заметил девочку.<br>
<br>
- На землю! Ложись! - крикнул он. - Куда тебя?!<br>
<br>
Афанасьев, подхватив ребенка, выскочил из огня, но перепуганная девочка рванулась
и побежала навтречу разрывам.<br>
<br>
- Стой!..<br>
<br>
Их разделало шагов пять, когда совсем рядом упала бомба. Качнулась земля - больше
Афанасьев ничего не помнил.<br>
<br>
В полевом госпитале установили: тяжелая контузия, полный паралич. Потянулись
мучительно долгие недели борьбы за жизнь. Временами Афанасьев приходил в себя:
он силился понять, как попал в эту больничную палату, что с ним случилось. Но
в висках гулко стучало, а в ушах стоял нескончаемый звон. Незнакомые люди в
белых халатах о чем-то переговаривались, но капитан не слышал ни звука. Тогда
вспоминались друзья, товарищи - Аркаша Шишов, Александр Пухов, Володя Говорун,
с кем работал в Оренбургском авиационном училище, с кем ходил на лихие штурмовки.
Где-то сейчас Филипп Пархоменко?.. Как он не хотел тогда фотографироваться перед
боевым вылетом...<br>
<br>
С тех пор как на попутной батальонной полуторке капитана Афанасьева увезли в госпиталь,
пилоыт не раз вылетали на связном По-2 в поисках комэска. Но кого ни спрашивали, никто
не знал о летчике-штурмовике. Не было его и в списках раненых, может быть, оттого, что
у раненого капитана не оказалось документов: в госпитале он числился только под порядковым
номером. Но крепкий организм победил недуг.<br>
<br>
И вот первые обнадеживающие симптомы: у раненого стали восстанавливаться речь, слух.
Врачи принялись учить летчика говорить. Когда же шевельнулись кисти рук, кто-то из
профессоров, обследовавших его, обращаясь к сопровождавшим врачам, неопределенно
обронил: "Через три года, возможно, поправится". Эту фрау случайно услышал Афанасьев.<br>
<br>
"Возможно, поправится", - сокрушался он.<br>
<br>
Не через три года, через три месяца подняся на ноги капитан Афанасьев! Да не просто
поднялся, но твердо решил вернуться в родную часть: ведь война еще не была закончена.
Путь к друзьям-однополчанам лежал через Москву, главные авиационные штабы, многие
кабинеты - заключение врачей было тому причиной. И все же настойчивый комэск своего добился:
где-то под Бродами он встретился с однополчанами.<br>
<br>
- А мы думали, не вернешься. Ну, давай отдыхай, набирайся сил. Потом - и домой! - решил
командир полка.<br>
<br>
- Как домой? Подхарчуем - летать будет! - скорей для поднятия духа товарища возразил
Пархоменко, но слова его тогда никто, конечно, всерьез не принял. Да и сам замкомэск
не предполагал, что пройдет несколько дней и капитан Афанасьев обратится с просьбой,
отказать в которой он будет не в силах.<br>
<br>
- Слетаем, Филипп. Не могу больше. Сидеть на земле, прятаться в щели, когда вы уходите
в бой... Ты же летчик, поймешь меня...<br>
<br>
В январе 1945 года следом за наступающими войсками 948-й штурмовой авиационный полк
перебазировался на запад. Афанасьева оставили на полевом аэродроме неподалеку от Брод.
"Будешь перегонять самолеты из ремонта", - сказали и улетели. Оказавшийся на аэродроме
комдив обратил внимание на прихрамывающего пилота с тросточкой.<br>
<br>
- После ранения?<br>
<br>
- Так точно, товарищ полковник. Перегоняю машины.<br>
<br>
- Прямо вот с палочкой и перегоняете? А ордена за что?<br>
<br>
- Это за боевые вылеты.<br>
<br>
- Сколько?<br>
<br>
- Больше семидесяти.<br>
<br>
Полковник перевел взгляд с орденов Афанасьева на трость, на которую опирался летчик,
и совершенно неожиданно предложил:<br>
<br>
- Бростье вы эту палку, капитан. И передайте командиру мое разрешение летать на
штурмовки. Пока хватит сил...<br>
<br>
Чужие дороги, чужие реки и озера видел он из кабины боевой машины. Злее, беспощаднее
становились атаки. А ночами, превозмогая боль от оставшихся в позвоночнике осколков,
он силился заснуть. Утром снова бои, снова штурмовки. Сто шестнадцать боевых вылетов
запишет в летной книжке к концу войны капитан Афанасьев, и к многочисленным наградам
прибавятся новые ордена и медали - за Вашаву, Берлин, Прагу, Вену. Где-то уже над Одером
случится такое, что потом будет ему вспоминаться долгие годы.<br>
<br>
Однако и в тот раз ему повезло.<br>
<br>
...На задание отправились двумя шестерками. Загрузив свой штурмовик бомбами сверх
расчетного, что бывало нечасто, комэск взлетел первым. Когда внизу промелькнули островерхие
крыши городка, неожиданно отказал мотор. Густая масляная пелена мгновенно забила козырек
кабины - пропала видимость. Леонид Афанасьев попытался было открыть фонарь - и тотчас
горячая черная жидкость ударила в лицо. Считанные секунды оставались до принятия решения.
Прыгать?.. Но под крылом - дома, люди. За бронеспинкой еще не догадывающийся о случившемся
стрелок.<br>
<br>
Катастрофически быстро падала высота. И вот осторожно со снижением самолет стал
разворачиваться на свой аэродром. "Прекратить взлет! - послышалась команда с земли. -
освободить полосу!" Комэск заметил, как в разные стороны торопливо поползли
готовые к вылету штурмовики. "А вдруг кто-то замешкается? Или того хуже - откажут
тормоза, и перегруженный бомбами самолет понесет на боевые машины?" - рассуждал Афанасьев.<br>
<br>
Полоса уже тянулась под крылом, когда Леонид Афанасьев принял решение, коорого, кажется,
сам испугался: сесть в стороне от полосы, в стороне от самолетов, сесть без шасси -
с шасси катастрофа неизбежна, - а значит, на "брюхо", на бомбы...<br>
<br>
Вышел Афанасьев живым из сотен атак. Всякое повидал за долгие месяцы войны.
И вот сейчас, когда войне близился конец, судьба преподнесла ему еще одно испытание,
как бы в насмешку за непокорность ей: чужие пули, снаряды и бомбы тебя не взяли,
а как против своих?..<br>
<br>
Первые минуты после остановки самолета летчик сидел неподвижно - было бессмысленно
суетиться, и он ждал. Ждал взрыва.<br>
<br>
Медленно плыли над головой облака, как белые корабли. Плыли к родным селам, чтобы росою
упасть на рассвете, туманом побродить над тихим озером, ливнем поплакатьгде-то над лесом...<br>
<br>
Прошла, кажется, вечность, когда Афанасьев поднялся из кабины на плоскость и понял, что
взрыва не будет: бомбы, падая, усеяли весь путь за машиной.<br>
<br>
Это уже потом можно было домысливать, что же спасло капитана Афанасьева: то ли
умелая посадка - юзом, едва ли не боком, то ли мягкий грунт, то ли простая случайность.
А тогда, подставив лицо полевому ветру, комэск думал: "Какая это радость - просто дышать
им, не учитывая поправки на снос бомб".<br>
<br>
...Минет много лет. Распашут фронтовые аэродромы. И бывший командир эскадрильи штурмовиков
композитор Афанасьев напишет песню, которую услышат и полюбят все. А друзьям-однополчанам
ее споет солист Большого театра Филипп Пархоменко. Песню о синих озерах, о невернувшихся
с войны соколах, об Иванах да Марьях, которыми сильна Россия.<br>
